package kunalDSA.string_stringbuilder;

public class PrettyPrinting {
    public static void main(String[] args) {


        //placeholders in java string
        float num = 45.2395f;
        System.out.printf("formatted number is %.2f",num);  //it will also round off.
        System.out.println();
        System.out.printf("%.3f",Math.PI);
        System.out.println();
        System.out.printf("my name is %s and i am from %s","sam","india");

    }
}
